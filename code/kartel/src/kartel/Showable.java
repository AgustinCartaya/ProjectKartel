
package kartel;

/**
 * Interface qui etablit les méthodes nécessaires pour
 * les objets qui peuvent seront montrés lors du jeu
 * 
 * @author Manel Meftah
 * @author Agustin Cartaya
 */
public interface Showable {
    
    /**
     * Methos que renvoi un texte avec ce qui sera montré
     * du joueur lors du jeu
     * @return String avec le texte a montrer lors du jeu
     */
    String show();
    
}
